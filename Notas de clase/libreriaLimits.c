/* La librería limits.h define los valores maximos y minimos de los tipos basicos de C.

Podemos usarla para cuando en una derivacion necesitamos utilizar -infinito o +infinito

1. Se incluye con "include <limits.h>"
2. Un ejemplo de uso sería:
    printf("The minimum value of INT = %d\n", INT_MIN)
3. El valor que nos dará será: -2147483648
4. El CHAR_MIN sería: -128, el CHAR_MAX sería: +127, el INT_MAX sería: +2147483647 */